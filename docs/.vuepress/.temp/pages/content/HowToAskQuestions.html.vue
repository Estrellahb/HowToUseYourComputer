<h2 id="怎么提问" tabindex="-1"><a class="header-anchor" href="#怎么提问"><span>怎么提问</span></a></h2>
<p>提出问题的<code v-pre>质量</code>决定了<code v-pre>回答的好坏</code>。
<br>
质量取决于你提出的问题是否言简意赅，能让人找到问题出在什么地方，并且能将你的问题精确描述，就像你对一个儿童对话一样，这会让你得到回答的概率大大增加。<br></p>
<h3 id="不要做以下的事情" tabindex="-1"><a class="header-anchor" href="#不要做以下的事情"><span>不要做以下的事情<br></span></a></h3>
<p><b>1. 要求他人立刻做出回复</b></p>
<p>并不是所有人都有充足的时间，也许对方在忙，耐心等待。即使没有人回复不要灰心，尝试自己解决（参考向谁提问）也许你会收获更多。</p>
<p><strong>2. 言辞尖锐，态度恶劣</strong><br>
在对话中威胁对方，要求对方必须解决，大部分人看到后对你产生厌恶进而拒绝回复。你只需要在语句中添加<code v-pre>请……</code> <code v-pre>谢谢</code>，会增加你收获答案的概率。</p>
<blockquote>
<p>反面案例：谁知道这东西上哪里找<br>
正面案例：请问这本书的资源能在什么平台获取到？【附上书名或图片】
<br></p>
</blockquote>
<p><strong>3. 问题中存在大量的语气词</strong><br>
诸如：请帮帮我……求求了……不解决真的会完蛋的……这样的提问并不能减少你获得答案的时间，反而回答者因为没有准确描述问题感到困扰，甚至拒绝回答。</p>
<h2 id="向谁提问" tabindex="-1"><a class="header-anchor" href="#向谁提问"><span>向谁提问</span></a></h2>
<ul>
<li><strong>搜索引擎</strong>
