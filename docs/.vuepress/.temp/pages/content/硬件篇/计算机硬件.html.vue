<h2 id="_2-显卡-gpu-图形处理器" tabindex="-1"><a class="header-anchor" href="#_2-显卡-gpu-图形处理器"><span>2. 显卡 (GPU) - 图形处理器</span></a></h2>
<p>显卡专门用于处理图形和图像相关任务的硬件。它常用于游戏、图形渲染或视频剪辑的任务。按类型分：</p>
<ul>
<li>
<p><strong>集成显卡</strong>：</p>
<ul>
<li>内置于 CPU 中，适合日常办公和轻度娱乐。</li>
<li>性能一般，无法流畅运行大型游戏或专业设计软件。</li>
</ul>
</li>
<li>
<p><strong>独立显卡</strong>：</p>
<ul>
<li>单独存在，性能强大，适合玩游戏、视频剪辑或 3D 渲染。</li>
<li>品牌：NVIDIA（如 GTX/RTX 系列）、AMD（如 Radeon 系列）和 Intel（锐炫Arc）。</li>
</ul>
</li>
<li>
<p><strong>主要性能参数</strong></p>
</li>
<li>
<p><strong>显存（VRAM）</strong>：显卡自带的专用内存，用于存储图像数据。显存越大，显卡在处理复杂任务时表现越好。</p>
</li>
<li>
<p><strong>CUDA核心/流处理器</strong>：这些是GPU内部的处理单元，数量越多，图形处理能力越强。</p>
</li>
<li>
<p><strong>带宽</strong>：显存和GPU之间的数据传输速率，带宽越高，图形处理效率越高。</p>
</li>
</ul>
